package SlidingWindow.KContagiousSubarray;

public class Array {

    public static void print_doubleArr(double[] arr)
    {
        for(int i=0; i<arr.length; i++)
        {
            System.out.println(arr[i]);
        }
    }

    public static void print_intArr(int[] arr)
    {
        for(int i=0; i<arr.length; i++)
        {
            System.out.println(arr[i]);
        }
    }
}
